use c006\products\models\GridViewAttr as GridViewAttrModel;
use Yii;
use yii\base\Model;
use yii\data\ActiveDataProvider;

/**
 * GridViewAttr represents the model behind the search form about `c006\products\models\GridViewAttr`.
 */
class GridViewAttr extends GridViewAttrModel
{
